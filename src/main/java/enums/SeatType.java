package enums;

/**
 * Enum to represent the different types of bus seats available for booking.
 * This helps to avoid hardcoding seat types across the framework and 
 * makes the code more readable and maintainable.
 */
public enum SeatType {

    SEATER,   // Represents a regular sitting seat

    SLEEPER   // Represents a sleeper seat (usually with reclining or lying option)

}
